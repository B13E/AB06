function sum(a, b) {
    const result = a + b;
    return result;
}


function minus(a, b) {
    const result = a - b;
    return result;
}


function multiply(a, b) {
    const result = a * b;
    return result;
}


function devide(a, b) {
    const result = a / b;
    return result;
}
module.exports = { sum, minus, multiply, devide};